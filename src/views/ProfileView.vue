    if (data?.status === 'success' && data?.data) {
      userInfo.value = data.data;
      // Êõ¥Êñ∞Êú¨Âú∞Â≠òÂÇ®
      localStorage.setItem('userInfo', JSON.stringify(data.data));
    }
  } catch (error) {
    // Ëé∑ÂèñÁî®Êà∑‰ø°ÊÅØÂ§±Ë¥•Ôºå‰ΩøÁî®Êú¨Âú∞Â≠òÂÇ®ÁöÑ‰ø°ÊÅØ
  }
}

// Êõ¥Êñ∞Áî®Êà∑ËØ≠Ë®ÄËÆæÁΩÆ
const updateUserLanguage = async (lang: string) => {
  try {
    // Âú®Êâ©Â±ïÁéØÂ¢É‰∏≠ÔºåÂè™Êõ¥Êñ∞Êú¨Âú∞Â≠òÂÇ®Ôºå‰∏çÂèëÈÄÅ API ËØ∑Ê±Ç
