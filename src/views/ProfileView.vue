  let lastProcessedTime = Date.now();

  // ÁõëÂê¨ËØ≠Ë®ÄÂèòÊõ¥‰∫ã‰ª∂
  window.addEventListener('language-changed', (event) => {
    const newLang = (event as CustomEvent).detail?.language || localStorage.getItem('language') || 'en-US';

