    currentLanguage.value = newLang;
    lastProcessedLang = newLang;
    lastProcessedTime = now;
  });

  // ÁõëÂê¨Âº∫Âà∂Âà∑Êñ∞‰∫ã‰ª∂
  window.addEventListener('force-refresh-i18n', () => {
